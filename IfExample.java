package IfElseStatement.ifStmnt;

public class IfExample {
	public static void main(String[] args) {
		
		int age =20;
		//check the condition.
		if (age>18) {
			System.out.println("Age is greater than 18");  // O/p :- Age is greater than 18.
			
			//check for less.
		
			if (age<30) {
				System.out.println("Age is less than 30."); // O/p :- Age is less than 30.
			}
			
		}
	}

}
